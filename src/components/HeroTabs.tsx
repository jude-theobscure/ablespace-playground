const TABS = [
  { label: "IEP Tracking",                 icon: <TargetIcon /> },
  { label: "Service Time & Accommodations", icon: <DateTimeIcon /> },
  { label: "Compliance",                    icon: <SecurityLockIcon /> },
  { label: "AbleSpace AI",                  icon: <AIIcon /> },
];

interface HeroTabsProps {
  onTabChange?: (index: number) => void;
}

export default function HeroTabs({ onTabChange }: HeroTabsProps) {
  const [active, setActive] = useState(0);
  const [indicator, setIndicator] = useState({ left: 0, width: 0 });
  const [timerKey, setTimerKey] = useState(0);
  const tabRefs = useRef<(HTMLButtonElement | null)[]>([]);
  const containerRef = useRef<HTMLDivElement>(null);
  const timerRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  useEffect(() => {
    const el = tabRefs.current[active];
    const container = containerRef.current;
    if (el && container) {
      const containerLeft = container.getBoundingClientRect().left;
      const elRect = el.getBoundingClientRect();
      setIndicator({ left: elRect.left - containerLeft, width: elRect.width });
    }
  }, [active]);

  const resetTimer = () => {
    if (timerRef.current) clearInterval(timerRef.current);
    setTimerKey(k => k + 1);
    timerRef.current = setInterval(() => {
      setActive((prev) => {
        const next = prev === TABS.length - 1 ? 0 : prev + 1;
        setTimeout(() => onTabChange?.(next), 0);
        return next;
      });
      setTimerKey(k => k + 1);
    }, 7000);
  };

  useEffect(() => {
    resetTimer();
    return () => { if (timerRef.current) clearInterval(timerRef.current); };
  }, []);

  const handleTabChange = (index: number) => {
    setActive(index);
    onTabChange?.(index);
    resetTimer();
  };

  const handlePrev = () => {
    const newIndex = active === 0 ? TABS.length - 1 : active - 1;
    handleTabChange(newIndex);
  };

  const handleNext = () => {
    const newIndex = active === TABS.length - 1 ? 0 : active + 1;
    handleTabChange(newIndex);
  };

  return (
    <div className="mt-32 flex items-center gap-3">
      {/* Left arrow */}
      <div className="relative w-11 h-11 flex-shrink-0">
        <button
          onClick={handlePrev}
          className="absolute inset-[3px] flex items-center justify-center rounded-full bg-[#FAF9F7] text-[#595550] hover:text-[#403B36] hover:bg-white transition-colors duration-200"
          aria-label="Previous tab"
        >
          <ArrowLeftIcon />
        </button>
        <svg className="absolute inset-0 pointer-events-none" width="44" height="44" viewBox="0 0 44 44">
          <circle cx="22" cy="22" r="20" fill="none" stroke="#E8E6E3" strokeWidth="2" />
        </svg>
      </div>

      {/* Tabs container */}
      <div
        ref={containerRef}
        className="relative flex items-center gap-1 p-1 rounded-[var(--radius-lg)] bg-[#FAF9F7]/50 border border-[#F2F1F1]"
      >
        {/* Sliding indicator */}
        <div
          className="absolute top-1 bottom-1 rounded-[var(--radius-md)] bg-white shadow-[var(--shadow-sm)] transition-all duration-200 ease-in-out"
          style={{ left: indicator.left, width: indicator.width }}
        />

        {TABS.map((tab, i) => (
          <button
            key={tab.label}
            ref={(el) => { tabRefs.current[i] = el; }}
            onClick={() => handleTabChange(i)}
            className={`relative z-10 flex items-center gap-1.5 px-4 py-2 rounded-[var(--radius-md)] text-sm font-medium whitespace-nowrap transition-colors duration-200 ${
              active === i ? "text-[#403B36]" : "text-[#595550] hover:text-[#403B36]"
            }`}
          >
            {tab.icon}
            {tab.label}
          </button>
        ))}
      </div>

      {/* Right arrow with circular progress ring */}
      <div className="relative w-11 h-11 flex-shrink-0">
        <button
          onClick={handleNext}
          className="absolute inset-[3px] flex items-center justify-center rounded-full bg-[#FAF9F7] text-[#595550] hover:text-[#403B36] hover:bg-white transition-colors duration-200"
          aria-label="Next tab"
        >
          <ArrowRightIcon />
        </button>
        <svg
          key={timerKey}
          className="absolute inset-0 -rotate-90 pointer-events-none"
          width="44"
          height="44"
          viewBox="0 0 44 44"
        >
          <circle cx="22" cy="22" r="20" fill="none" stroke="#E8E6E3" strokeWidth="2" />
          <circle
            cx="22" cy="22" r="20"
            fill="none"
            stroke="#C7BBAE"
            strokeWidth="2"
            strokeLinecap="round"
            strokeDasharray="125.66"
            strokeDashoffset="125.66"
            style={{ animation: "tab-progress 7s linear forwards" }}
          />
        </svg>
      </div>
    </div>
  );
}
